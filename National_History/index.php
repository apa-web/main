					<b>History of Alpha East</b></br></br>
History of the Eastern Region of Alpha Phi Alpha Fraternity, Inc.<br><br>

Alpha Phi Alpha Fraternity, Inc. was founded on December 4, 1906 in Ithaca, NY, making what is now known as the Eastern Region,
the birth place of Alpha. The region comprises of 12 states covering the geographical areas between Virginia and Maine and also 
includes Bermuda and Germany. The Eastern Region was monumental to the expansion, development and sustainability of Alpha Phi Alpha, 
especially in its early stages. It was here that the fraternity was incorporated, held its first national convention and began 
expansion by establishing chapters on other college campuses such as Howard University, Virginia Union University, Yale University, 
Columbia University, University of Pennsylvania, among many others.
<br><br>
AlphaEast is also the home of the National Headquarters in Baltimore, Maryland. 
The region currently has 148 active chapters and over 3,000 active members. AlphaEast takes great pride in being "First of All, Leading the Way."
</br></br>
 
History of ONECA (Organization of New England Chapters of Alpha)<br><br>
The concept or "birth" of ONECA came about as the result of the Chapters in VA, who were the first of the Districts 
(of which there are 7 in the Eastern Region) to use an acronym to identify themselves. They chose the acronym VACAPAF,
 which stands for the Virginia Chapters of Alpha Phi Alpha Fraternity.<br><br>
During a "brain storming" session with other District II Chapters (as all Chapters were only known by a District number), 
Bro. Thomas Harris IV suggested the acronym of ONECA and later presented it at the following District II Conference. 
Bro. Harris was President of the BSL (Hartford Alumni) Chapter at the time, and after a vote around 1979-1980, 
the Organization of New England Chapters of Alpha (ONECA) was born.
<br><br>
There are 6 states that make up ONECA: CT, MA, RI, NH, ME, and VT. Instrumental Chapters in the foundation building along with BSL, were HAL (New Haven, CT), TIL (Springfield, MA), ZPL (Stanford, CT), EGL (Boston, MA) and AG (Brown University).
<br><br>
As we stand today, close to 30 years later, ONECA is still strong and vibrant in the Greater New England area with 15 Chapters performing the work of Alpha.
<br><br>
The whistle has sounded and the call is made: "ALL ABOARD! The ONECA Train is leaving the station and is coming through!"

