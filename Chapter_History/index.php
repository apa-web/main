				<p>
Some of the most prominent and influential men of color were initiated into Alpha through Sigma chapter. For example, <b>Silas "Shag" Taylor</b> ('23), who by the time of his 1937 appointment to the Massachusetts Parole Board, had become one of the Boston metro-area's most powerful political bosses.
<br><br>
<b>Brother Julian D. Rainey</b> ('23), became, in 1925, the first person of color to become faculty of a leading law school when he became a member of the Suffolk Law School faculty. Rainey went on to run for City Council in 1925 and finished a close second.
<br><br>
Another son of Sigma to rise to prominence was, ex-president <b>Clifton R. Wharton</b>, who was appointed secretary to the Legation in Liberia, "where he would take up great work in the service of his country."
<br><br>
<b>Ferdinand Rousseve</b>, while a member of Sigma Lambda Chapter, designed the Second Fraternity Shield; this revised Shield is the one that is currently used by the organization.
<br><br>
When the General Organization decided to pursue a variety of insurance programs, one of the insurance companies it entered a business relationship with was Atlanta Life Insurance Company, which was founded by <b>A. F. Herndon</b> and operated by his son, Sigma alumnus <b>Norris Herndon</b> (Harvard,'21).
<br><br>
<b>Brother Charles Hamilton Houston</b> (Harvard, '21), first black editor of the Harvard Law Review, envisaged of and led the legal strategy leading to the end of legalized racial segregation in the United States. He was one of the individuals whom laid the legal groundwork through thought and action that ultimately led to 1954 U.S. Supreme Court decision in Brown v. Board of Education that made racial segregation in public primary and secondary schools unconstitutional. Brother Houston not only participated in effecting the change, but was the inspiration and mentor to Brother Thurgood Marshall, and many others who carried on the battle and remains an inspiration to those working for social justice today.
<br><br>
<b>Brother Alonzo G. Moron</b> (Brown, Spr. '30) was the first black president of Hampton University, influencing the civil rights movement as he worked to upgrade Hampton Institute from a trade school to a college. He proved that a school for African Americans could be successful with a black man in charge. Moron's influence was significant in many fields. An outstanding scholar, effective administrator, and advocator for civil rights, he held many groundbreaking posts, all with distinction.
<br><br>
<b>Brother Robert C. "Bobby" Scott</b> (Harvard, Spr. '66) is a Congressman in Virginia's Third Congressional District. As a legislator for the people, he has passed laws that improved healthcare benefits for women, infants and children, increased the Virginia minimum wage, created the Governor's Employment and Training Council and established The Neighborhood Assistance Act (which provides tax credits to businesses for donations made to approved social service and crime prevention programs).
<br><br>
<b>Brother Wendell Knox</b> (Harvard, Spr. '66), President and CEO of Abt Associates, is a Harvard University graduate. Brother Knox is active in civic affairs, including board memberships with Brigham and Women's Hospital, The Greater Boston Chamber of Commerce, The Partnership, The Biomedical Sciences Career Program, The National Conference for Community and Justice, and the Dimock Community Foundation. He also serves on the boards of Eastern Bank and Allmerica Financial Corporation.
<br><br>
<b>Brother John Cawthorne</b> (Harvard, Spr. '61) is an Associate Dean of Students and Outreach, the Lynch School of Education at Boston College. Prior to joining the administration of the Lynch School at Boston College, Brother Cawthorne was vice president for education of the National Urban League, Research Professor at Boston College, and Senior Research Associate in the Center for the Study of Testing, Evaluation, and Educational Policy (CSTEEP) at the Lynch School. A native of Oklahoma, Brother Cawthorne began his teaching career, fresh out of Harvard, in 1964 in Washington, D.C. He has run a federal program for primary students in Washington, consulted with a number of national foundations and school districts, was principal of the Lower School of the Massachusetts Experimental School system, and was director of Chapter I for the Massachusetts Department of Correction, where he was responsible for educational programs for inmates in the Commonwealth's prisons and jails under the age of 21.
<br><br>
<b>Brother Antonio Hart</b> (Berklee College of Music, Spr. '87) has long been recognized as one of the most talented instrumentalist of his generation. In recent years he has also been recognized as an important Jazz educator. In his 20 years as a professional musician he has performed and recorded with many jazz greats, from Dizzy Gillespie, Roy Hargrove, Nancy Wilson, Nat Adderley, and McCoy Tyner to Dave Holland.
<br>
</p>
