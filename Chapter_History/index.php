The objectives of Alpha Phi Alpha Fraternity, Inc. are to stimulate the ambition of its members; to prepare them for the greatest usefulness 
in the cause of humanity, freedom, and dignity of the individual;to encourage the highest and noblest form of manhood; and to aid downtrodden 
humanity its efforts to achieve higher social, economic, and intellectual status.</br>
</br> 
The first two objectives - (1) to stimulate the ambition of its members and (2) to prepare them for the greatest 
usefulness in the cause of humanity, freedom, and dignity of the individual-serve as the basis for the establishment of Alpha University.
Since the establishment of Sigma Chapter, Brothers have actively engaged in community service activities and developed programs that have 
made it a pillar in the city of Boston on both the collegiate and community level. Sigma Chapter prides itself on over 96 years of tradition 
and values itself in upholding the ideals of the fraternity.</br> 
</br> 
Sigma Chapter encompasses the Boston metro region and continues to be a force within the community. 
Some of the current programs in which the chapter participates in or coordinates are: 
Feeding the homeless at Church of All Nations, Miss Black and Gold, Go to High School-Go to College, the March of Dimes, and A Phi A Little Brother's Day.
