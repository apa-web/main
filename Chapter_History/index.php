The fall of 1915 found these seven men of Alpha Phi Alpha Fraternity Inc., each of different backgrounds and environments,
gathered in Boston, Massachusetts. Having recently graduated from Lincoln University of Pennsylvania, Yale University of New Haven,
Connecticut and Howard University of Washington, D.C., they each arrived in Boston, a city that has long been a bastion for
the advancement of the Negro. On November 28, 1915 these seven men banded together to charter the Sigma Chapter of Alpha Phi Alpha Fraternity.
Since being established in 1915, Sigma Chapter has had a very rich tradition and proud heritage.
Unlike any other chapter in Alpha Phi Alpha, it has the distinction of being the only chapter to be chartered during the tenure of a Jewel
as General President (Jewel Henry A. Callis). Over the past 95 years, Sigma has given birth to notable Alpha men that have shaped the lives of
Americans across the United States. Perhaps the most notable son of Sigma is slain civil rights activist Dr. Martin Luther King, Jr. (May 13, 1952).
Though Dr. King is the most recognized son, Sigma has noble sons that have matured to become Masons, Congressmen, Chief Executive Officers,
prominent businessmen, musicians, scholars and professionals. Sons of Sigma have continued to hold the Light of Alpha high by excelling in each and every
undertaking they pursue. Whether a basketball tournament, Go-to-High School, Go-to-College campaign or a "party walk," the Brothers of Sigma Chapter
have always had a penchant for success.
